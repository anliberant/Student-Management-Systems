            pst.setString(6, average.getText());
            pst.setInt(7, Integer.parseInt(ranking.getText()));
            pst.setString(8, maths.getText());
            pst.setString(9, english.getText());
            pst.setString(10, biology.getText());
            pst.setString(11, computing.getText());
            pst.setString(12, chemistry.getText());
            pst.setString(13, physics.getText());
            pst.setString(14, addMaths.getText());
            pst.setString(15, business.getText());

            pst.execute();
            JOptionPane.showMessageDialog(null, "System Update Completed");

            rs.close();
            pst.close();

            //updateTable();
