    private void updateTable() {
        conn = StudentData.connectDB();
        if (conn != null) {
            String sql = "SELECT StudentID,Course,TotalScore,Average,Ranking,Maths,English,Biology,Computing,Chemistry,Physics,AddMaths,Business FROM Student";
            try {
                pst = conn.prepareStatement(sql);
                rs = pst.executeQuery();
                Object[] columnData = new Object[13];

                while (rs.next()) {
                    columnData[0] = rs.getString("StudentID");
                    columnData[1] = rs.getString("Course");
                    columnData[2] = rs.getString("TotalScore");
                    columnData[3] = rs.getString("Average");
                    columnData[4] = Integer.parseInt(rs.getString("Ranking"));
                    columnData[5] = rs.getString("Maths");
                    columnData[6] = rs.getString("English");
                    columnData[7] = rs.getString("Biology");
                    columnData[8] = rs.getString("Computing");
                    columnData[9] = rs.getString("Chemistry");
                    columnData[10] = rs.getString("Physics");
                    columnData[11] = rs.getString("AddMaths");
                    columnData[12] = rs.getString("Business");

                    model.addRow(columnData);

                }
            } catch (NumberFormatException | SQLException e) {
            }
        }
    }

